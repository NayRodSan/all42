#include <unistd.h>

int	main(void)
{
	int nbr;

	nbr = 57;
	while (nbr <= 57 && nbr >= 48)
	{
		write(1, &nbr, 1);
		nbr--;
	}
	if (nbr == 48)
	{
		write(1, "\n", 1);
	}
	return (0);
}
